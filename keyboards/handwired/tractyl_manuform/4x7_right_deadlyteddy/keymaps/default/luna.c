void render_luna(void) {
    if (timer_elapsed32(luna_timer) > LUNA_FRAME_MS) {
        luna_timer = timer_read32();
        luna_frame = (luna_frame + 1) % 2;
    }

    oled_set_cursor(LUNA_X, LUNA_Y);

    uint8_t wpm = get_current_wpm();
    led_t led = host_keyboard_led_state();

    if (led.caps_lock) {
        oled_write_raw_P(bark[luna_frame], LUNA_SIZE);
    } else if (wpm <= LUNA_WALK) {
        oled_write_raw_P(sit[luna_frame], LUNA_SIZE);
    } else if (wpm <= LUNA_RUN) {
        oled_write_raw_P(walk[luna_frame], LUNA_SIZE);
    } else {
        oled_write_raw_P(run[luna_frame], LUNA_SIZE);
    }
}
